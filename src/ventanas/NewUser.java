    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int permisos_box, validacion = 0;
        String nombre, email, telefono, username, pass, permisos_string = "";
        
        email = txteMail.getText().trim();
        nombre = txtNombre.getText().trim();
        telefono = txtPhone.getText().trim();
        username = txtUser.getText().trim();
        pass = txtPass.getText().trim();
        permisos_box = boxNiveles.getSelectedIndex() + 1;
        
        if(email.equals("")){
            txteMail.setBackground(Color.MAGENTA);
            validacion++;
        }
        if(nombre.equals("")){
            txtNombre.setBackground(Color.MAGENTA);
            validacion++;
        }
        if(telefono.equals("")){
            txtPhone.setBackground(Color.MAGENTA);
            validacion++;
        }
        if(username.equals("")){
            txtUser.setBackground(Color.MAGENTA);
            validacion++;
        }
        if(pass.equals("")){
            txtPass.setBackground(Color.MAGENTA);
            validacion++;
        }
        
        if(permisos_box == 1){
            
            permisos_string = "Administrador";
            
        }else if(permisos_box == 2){
            
            permisos_string = "Vendedor";
            
        }else if(permisos_box == 3){
            
            permisos_string = "Tecnico";
            
        }
        
        try {
            
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
            "select username from usuarios where username = '" + username + "'");
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                
                txtUser.setBackground(Color.MAGENTA);
                JOptionPane.showMessageDialog(null, "Nombre de Usuario no disponible.");
                cn.close();
                
            } else {
                
                cn.close();
                
                if (validacion == 0) {
                    
                    try {
                        
                        Connection cn2 = Conexion.conectar();
                        PreparedStatement pst2 = cn2.prepareStatement(
                        "insert into usuarios values (?,?,?,?,?,?,?,?,?)");
                        
                        pst2.setInt(1, 0);
                        pst2.setString(2, nombre);
                        pst2.setString(3, email);
                        pst2.setString(4, telefono);
                        pst2.setString(5, username);
                        pst2.setString(6, pass);
                        pst2.setString(7, permisos_string);
                        pst2.setString(8, "Activo");
                        pst2.setString(9, user);
                        
                        pst2.executeUpdate();
                        cn2.close();
                        
                        Limpiar();
                        
                        txtNombre.setBackground(Color.GREEN);
                        txtPass.setBackground(Color.GREEN);
                        txtPhone.setBackground(Color.GREEN);
                        txtUser.setBackground(Color.GREEN);
                        txteMail.setBackground(Color.GREEN);
                        
                        JOptionPane.showMessageDialog(null, "Registro Exitoso.");
                        
                        this.dispose();
                        
                        
                    } catch (SQLException e) {
                        
                        System.err.println("Error en conectar a la base de datos " + e);
            JOptionPane.showMessageDialog(null, "Error con la base de datos, Contacte al addministrador.");
                        
                    }
                    
                } else {
                    
                    JOptionPane.showMessageDialog(null, "Debe llenar todos los campos requeridos");
                    
                }
                
            }
            
        } catch (SQLException e) {
            System.err.println("Error en validar nombre de usuario " + e);
            JOptionPane.showMessageDialog(null, "Error al comparar Usuario, Contacte al addministrador.");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


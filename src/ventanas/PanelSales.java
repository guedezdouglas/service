    private void botonImprimirClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonImprimirClientesActionPerformed
        Document doc = new Document();
        
        try {
            String ruta = System.getProperty("user.home");
            PdfWriter.getInstance(doc, new FileOutputStream(ruta + "/Reportes/Clientes/Lista_Clientes.pdf"));
            
            com.itextpdf.text.Image header = com.itextpdf.text.Image.getInstance("src/images/BannerPDF.png");
            header.scaleToFit(650, 1000);
            header.setAlignment(Chunk.ALIGN_CENTER);
            
            Paragraph pf = new Paragraph();
            pf.setAlignment(Paragraph.ALIGN_CENTER);
            pf.add("Lista de Clientes. \n \n");
            pf.setFont(FontFactory.getFont("Tahoma", 18, Font.BOLD, BaseColor.DARK_GRAY));
            
            doc.open();
            doc.add(header);
            doc.add(pf);
            
            PdfPTable tablaClientes = new PdfPTable(5);
            
            tablaClientes.addCell(" ID# ");
            tablaClientes.addCell("Nombre");
            tablaClientes.addCell("eMail");
            tablaClientes.addCell("Telefono");
            tablaClientes.addCell("Direccion");
            
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(
                "select * from clientes");
                ResultSet rs = pst.executeQuery();
                
                if (rs.next()) {
                    do {                        
                        tablaClientes.addCell(rs.getString(1));
                        tablaClientes.addCell(rs.getString(2));
                        tablaClientes.addCell(rs.getString(3));
                        tablaClientes.addCell(rs.getString(4));
                        tablaClientes.addCell(rs.getString(5));
                    } while (rs.next());
                    doc.add(tablaClientes);
                }
            } catch (SQLException e) {
                System.err.println("Error al generar lista de clientes \n" + e);
            }
            doc.close();
            JOptionPane.showMessageDialog(null, "Lista de Clientes creado correctamente");
            
        } catch (Exception e) {
            System.err.println("Error al generar PDF" + e);
        }        
    }//GEN-LAST:event_botonImprimirClientesActionPerformed


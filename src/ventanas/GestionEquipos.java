        public void EscuchaTabla() {
                tablaEquipos.addMouseListener(new MouseAdapter() {
                        @Override
                        public void mouseClicked(MouseEvent e) {
                                int fila_point = tablaEquipos.rowAtPoint(e.getPoint());
                                int columna_point = 0;

                                if (fila_point > -1) {
                                        IDEquipo_update = (int) model.getValueAt(fila_point, columna_point);
                                        new FichaEquipoTec().setVisible(true);
                                }
                        }
                });
        }

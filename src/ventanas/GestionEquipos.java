        private void botonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMostrarActionPerformed
                String estatus = boxEstatus.getSelectedItem().toString();
                String query = "";

                model.setRowCount(0);

                try {
                        Connection cn = Conexion.conectar();

                        if (estatus.equals("Todos")) {
                                query = "select id_equipos, tipo_equipo, marca, estatus from equipos";
                        } else {
                                query = "select id_equipos, tipo_equipo, marca, estatus from equipos where estatus = '" + estatus + "'";
                        }

                        PreparedStatement pst = cn.prepareStatement(query);
                        ResultSet rs = pst.executeQuery();

                        while (rs.next()) {
                                Object[] fila = new Object[4];

                                for (int i = 0; i < 4; i++) {
                                        fila[i] = rs.getObject(i + 1);
                                }
                                model.addRow(fila);
                        }
                        cn.close();

                } catch (SQLException e) {
                        System.err.println("Error al obtener datos para la tabla equipos. " + e);
                }
        }//GEN-LAST:event_botonMostrarActionPerformed


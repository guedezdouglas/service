    public PanelAdmin() {
        initComponents();
        user = VentanaLogin.user;
        sesion_usuario = 1;
        this.setSize(650, 430);
        this.setResizable(false);
        this.setLocationRelativeTo(null);
        this.setTitle("Panel de Administrador. Sesion - " + user);
        this.setDefaultCloseOperation(WindowConstants.EXIT_ON_CLOSE);
        
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(lblWallpaper.getWidth(),
                lblWallpaper.getHeight(), Image.SCALE_DEFAULT));
        lblWallpaper.setIcon(icono);
        this.repaint();
        
        try{
            
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
            "select nombre_usuario from usuarios where username = '" + user + "'");
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                
                nombre_usuario = rs.getString("nombre_usuario");
                lblNombreUsuario.setText(nombre_usuario);
                
            }
            
        }catch(SQLException e){
            
            JOptionPane.showMessageDialog(null, "Error en conexion: " + e);
            
        }
        
    }
    

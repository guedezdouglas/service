    private void BotonAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAccederActionPerformed

        user = txtUsuario.getText().trim();
        pass = txtPass.getText().trim();

        if (!user.equals("") || !pass.equals("")) {

            try {

                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(
                        "select tipo_nivel, estatus from usuarios where username = '" + user
                        + "' and password = '" + pass + "'");

                ResultSet rs = pst.executeQuery();
                if (rs.next()) {

                    String nivel = rs.getString("tipo_nivel");
                    String status = rs.getString("estatus");

                    if (nivel.equals("Administrador") && status.equals("Activo")) {

                        dispose();
                        new PanelAdmin().setVisible(true);

                    } else if (nivel.equals("Vendedor") && status.equals("Activo")) {

                        dispose();
                        new PanelSales().setVisible(true);

                    } else if (nivel.equals("Tecnico") && status.equals("Activo")) {

                        dispose();
                        new PanelTech().setVisible(true);

                    }

                } else {

                    JOptionPane.showMessageDialog(null, "Datos de Acceso incorrectos.");
                    txtPass.setText("");
                    txtUsuario.setText("");

                }

            } catch (SQLException e) {
                System.err.println("Error en Acceder: " + e);
                JOptionPane.showMessageDialog(null, "Error al iniciar sesion, contacte al Administrador.");
            }

        } else {
            JOptionPane.showMessageDialog(null, "No dejar campos vacios, intente de nuevo.");
        }

    }//GEN-LAST:event_BotonAccederActionPerformed

